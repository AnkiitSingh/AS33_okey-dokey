import { API } from '../Api';

const signin = (user) => {
    return fetch(`${API}/getNgo/ngoLogin`, {
        method: "POST",
        headers: {
            Accept: "application/json",
            "Content-Type": "application/json"
        },
        body: JSON.stringify(user)
    })
        .then(response => {
            return response.json();
        })
        .catch(err => console.log(err));
};

export default signin;